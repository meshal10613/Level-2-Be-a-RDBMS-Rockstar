--? Aggregate Functions
-- avg, min, max, sum, count

-- avg
select avg(age) from students; -- return only one value

-- min
select min(age) from students; -- return only one value

-- max
select max(age) from students; -- return only one value

-- sum
select sum(age) from students; -- return only one value

-- count
select count(age) from students; -- return only one value